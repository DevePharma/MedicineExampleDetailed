function App() {
  const [selectedCategory, setSelectedCategory] = useState("");
  const [filteredMedications, setFilteredMedications] = useState(medications);

  // Get unique categories
  const categories = [...new Set(medications.map((item) => item.category))];

  // Handle dropdown selection
  const handleCategoryChange = (event) => {
    const category = event.target.value;
    setSelectedCategory(category);
  };

  // Update filtered medications whenever the category changes
  useEffect(() => {
    const filtered = medications
      .filter(
        (med) => selectedCategory === "" || med.category === selectedCategory
      )
      .sort((a, b) => (a.type === "SL" ? -1 : b.type === "SL" ? 1 : 0));

    setFilteredMedications(filtered);
  }, [selectedCategory]); // Runs every time selectedCategory changes
  return (
    <>
      <label htmlFor="category">Select Category:</label>
      <select
        id="category"
        value={selectedCategory}
        onChange={handleCategoryChange}
      >
        <option value="">-- Select a Category --</option>
        {categories.map((category, index) => (
          <option key={index} value={category}>
            {category}
          </option>
        ))}
      </select>

      <MedicationList medications={filteredMedications} />
    </>
  );
}

export default App;
